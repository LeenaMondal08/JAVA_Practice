class Bitoprator{
    public static void main(String args[]){
        int n1=9;
        int n2=6;
        System.out.println(n1|n2);
        System.out.println(n1&n2);
        System.out.println(~n1);
        System.out.println(n1^n2);
        System.out.println(n1<<2);
        System.out.println(n2>>2);



    }
}